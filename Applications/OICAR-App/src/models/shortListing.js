class ShortListing {

    constructor(id, title, category, price, pricePeriod, rating, image, manufacturer, model) {
        this.id = id,
        this.title = title,
        this.category = category,
        this.price = price,
        this.pricePeriod = pricePeriod,
        this.rating = rating,
        this.image = image,
        this.manufacturer = manufacturer,
        this.model = model
    }

}

export default ShortListing;